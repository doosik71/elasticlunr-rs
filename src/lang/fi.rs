use super::{
    common::{RustStemmer, StopWordFilter, Trimmer},
    Language,
};
use crate::pipeline::Pipeline;
use rust_stemmers::Algorithm;

pub struct Finnish {}

impl Finnish {
    pub fn new() -> Self {
        Self {}
    }
}

impl Language for Finnish {
    fn name(&self) -> String {
        "Finnish".into()
    }
    fn code(&self) -> String {
        "fi".into()
    }

    fn tokenize(&self, text: &str) -> Vec<String> {
        super::tokenize_whitespace(text)
    }

    fn make_pipeline(&self) -> Pipeline {
        Pipeline {
            queue: vec![
                Box::new(Trimmer::new("trimmer-fi", WORD_CHARS)),
                Box::new(StopWordFilter::new("stopWordFilter-fi", STOP_WORDS)),
                Box::new(RustStemmer::new("stemmer-fi", Algorithm::Finnish)),
            ],
        }
    }
}

